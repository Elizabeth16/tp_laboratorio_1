/** \brief Solicita un numero y lo retorna
 * \param
 * \param
 * \return Numero ingresado por el Usuario
 */
float pedirNumero();
/** \brief Solicita al Usuario la suma de A+B
 * \param  numeroA ingresado por el usuario
 * \param  numeroB ingresado por el usuario
 * \return Resultado de la suma
 */
float suma(float numeroA, float numeroB);
/** \brief Solicita al Usuario la resta de A-B
 * \param  numeroA ingresado por el usuario
 * \param  numeroB ingresado por el usuario
 * \return Resultado de la resta
 */
float resta(float numeroA, float numeroB);
/** \brief Solicita al Usuario la multiplicacion de A*B
 * \param  numeroA ingresado por el usuario
 * \param  numeroB ingresado por el usuario
 * \return Resultado de la multiplicacion
 */
float multiplica(float numeroA, float numeroB);
/** \brief Solicita al Usuario la division de A/B. Con error de division por cero
 * \param  numeroA ingresado por el usuario
 * \param  numeroB ingresado por el usuario
 * \return Resultado de la division. O Error de division por cero
 */
float divide(float numeroA, float numeroB);
/** \brief Solicita al Usuario el factorial de A!
 * \param  numeroA ingresado para el factorial A
 * \param
 * \return Resultado de factorial A
 */
float factorialA(float numeroA);
/** \brief Solicita al Usuario el factorial de B!
 * \param numeroB ingresado para el factorial B
 * \param
 * \return Resultado de factorial B
 */
float factorialB(float numeroB);
